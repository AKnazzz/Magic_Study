package org.hogwarts.a1_types_of_variables_1;

import java.util.Random;

/**
 * Теперь представим, что Макгонагалл выставляет оценки за работу с помощью магического пера. Перо умеет проверять само — оно отлично знает материал.
 * <p>
 * Давайте поставим оценки за две работы. Для этого будем использовать класс Random.
 * <p>
 * С помощью new Random().nextInt(1, 10) сгенерируйте 2 разных числа grade1, grade2. Обратите внимание, что класс Random вообще призван,
 * чтобы всегда генерировать случайное число. Тут мы указали конкретный диапазон, потому что работу можно оценить от 1 до 10.
 * Но можно диапазон и не указывать. Каждый раз, когда мы будем вызывать этот метод, будет генерироваться новое число.
 * <p>
 * Теперь давайте проверим, что оба числа больше 5 — это приемлемая для сдачи работы оценка. Используем операции сравнения и &&, чтобы выполнить две проверки на одной строчке.
 * Результат выполнения записываем в переменную типа boolean c названием result.
 * Затем выводим result на экран. Так, если оба числа были больше 5, в результате мы должны получить true.
 * Чтобы проверить, какими были числа, можно перед операцией сравнения тоже вывести их на экран.
 * <p>
 * Затем посчитаем средний балл: сложим две оценки и разделим на два. Подумать, какой тип данных может получиться в итоге? Результат также вывести на экран.
 * Запустите приложение несколько раз, посмотрите, как изменяются данные в консоли.
 */

public class Hogwarts12 {
    public static void main(String[] args) {
        Random random = new Random();

        int grade1 = random.nextInt(10) + 1; // Генерируем оценку за первую работу от 1 до 10
        int grade2 = random.nextInt(10) + 1; // Генерируем оценку за вторую работу от 1 до 10

        System.out.println("Оценка за первую работу: " + grade1);
        System.out.println("Оценка за вторую работу: " + grade2);

        boolean result = grade1 > 5 && grade2 > 5; // Проверяем, что обе оценки больше 5
        System.out.println("Обе оценки больше 5: " + result);

        double averageGrade = (grade1 + grade2) / 2.0; // Вычисляем средний балл
        System.out.println("Средний балл: " + averageGrade);
    }
}
